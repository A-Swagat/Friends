package com.swagat.friends;

public class FriendsData {
    private String name;
    private String url;

    //Empty constructor
    FriendsData(){}


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
